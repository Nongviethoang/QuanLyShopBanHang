    private void jButtonXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXoaActionPerformed
        int d = jTableQlsp.getSelectedRow();
        if (d >= 0) {
            SanPham sp = dssp.get(d);
            int op = JOptionPane.showConfirmDialog(rootPane, "Bạn có thực sự muốn xóa", "Xóa sản phẩm", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                dssp.remove(sp);
            }
            if (new ConnectDB().xoaSP(sp)) {
                JOptionPane.showMessageDialog(rootPane, "Đã xóa sản phẩm " + sp.getTenSP());
                getData();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Xóa thất bại");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Chưa có sản phẩm để xóa");
        }
    }//GEN-LAST:event_jButtonXoaActionPerformed

