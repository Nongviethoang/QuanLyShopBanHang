    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed

        SanPham sp;

        
        String tensp = txtTensp.getText();
        if (tensp.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, " Tên sản phẩm còn trống");
            return;
        }
        String thuonghieu = cbbThuongHieu.getSelectedItem().toString();
        if (thuonghieu.trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Thương hiệu còn trống!");
        }
        double gianhap = Double.parseDouble(txtGiaNhap.getText());
        if ((gianhap+"").trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Giá nhập còn trống!");
        }
        double giaban = Double.parseDouble(txtGiaBan.getText());
        if ((giaban+"").trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Giá bán còn trống!");
        }
        int slc = Integer.parseInt(txtSoluongcon.getText());
        
        sp = new SanPham(tensp, thuonghieu, gianhap, giaban, slc);
        dssp.add(sp);
        
        if (new ConnectDB().themSP(sp)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
        getData();
        txtMasp.setText("");
        txtTensp.setText("");
        cbbThuongHieu.setSelectedIndex(10);
        txtGiaNhap.setText("");
        txtGiaBan.setText("");
        txtSoluongcon.setText("");
    }//GEN-LAST:event_jButtonThemActionPerformed

