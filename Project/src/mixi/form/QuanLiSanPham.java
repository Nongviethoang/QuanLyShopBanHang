    private void LayDuLieuChiTietPhieuNhap(String maPN) {
        String cautruyvan = "select MaCTPN,MaPhieuNhap,SanPham.TenSanPham,SoLuong,TongTien "
                + " from ChiTietPhieuNhap,SanPham where ChiTietPhieuNhap.MaSanPham=SanPham.MaSanPham  and MaPhieuNhap=" + maPN;
        ResultSet rs = new ConnectDB().ExcuteQueryGetTable(cautruyvan);
        Object[] obj = new Object[]{"Mã CTPN", "Mã Phiếu nhập", "Tên Sản Phẩm", "Số Lượng", "Thành tiền"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        jTableCTPhieuNhap.setModel(tableModel);
        try {
            while (rs.next()) {
                Object[] item = new Object[5];
                item[0] = rs.getInt("MaCTPN");
                item[1] = rs.getString("MaPhieuNhap");
                item[2] = rs.getString("TenSanPham");
                item[3] = rs.getString("SoLuong");
                item[4] = rs.getDouble("TongTien");
                tableModel.addRow(item);
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }
