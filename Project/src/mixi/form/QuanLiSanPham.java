    public void loadDataCbb(String sql, JComboBox cbb, String tenBien){
        
        try {
            
//            String sql = "select * from NHACUNGCAP";
            Connection cn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=QuanLySanPhamMixiShop", "sa", "123456");
            try {
                PreparedStatement ps = cn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                while (rs.next()){
                    cbb.addItem(rs.getString(tenBien));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Thông báo lỗi", 1);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(QuanLiSanPham.class.getName()).log(Level.SEVERE,null, ex);
            
        }
    }
    
